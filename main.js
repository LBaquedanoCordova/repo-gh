function isNumPar(n) {
    return (n % 2) === 0;
}

function sumar(a, b) {
    return Number.parseFloat(a) + Number.parseFloat(b);
}


function restar(a, b) {
    return Number.parseFloat(a) - Number.parseFloat(b);
}

function multplicar(a, b) {
    return Number.parseFloat(a) * Number.parseFloat(b);
}
